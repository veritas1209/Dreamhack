# 자네가 수거해온 적의 탄약 35발
data = [
    0x96, 0x44, 0x5B, 0x25, 0x47, 0x8C, 0x59, 0x25, 
    0x92, 0x5A, 0x25, 0x41, 0xF0, 0x44, 0x27, 0xF0, 
    0x8C, 0x4C, 0x4C, 0x29, 0x59, 0x27, 0x25, 0x29, 
    0x2C, 0x59, 0x27, 0x76, 0x8C, 0x27, 0x5A, 0x25, 
    0x29, 0xC7, 0x29
]

def find_inverse(val, mod):
    # (Input * val) % mod == 1 인 Input 찾기
    for i in range(1, mod):
        if (i * val) % mod == 1:
            return i
    return None

flag = ""
for d in data:
    res = find_inverse(d, 0xFB)
    if res:
        flag += chr(res)
    else:
        flag += "?"

print(f"\n[+] 적 대공포 체계 완전 궤멸!")
print(f"[+] 획득한 플래그: {flag}")